public class Tabel {
    int teller;
    int vergelijkingen;

    public Tabel(int teller, int vergelijkingen){
        this.teller = teller;
        this.vergelijkingen = vergelijkingen;
    }

    public int getTeller(){
        return teller;
    }

    public int getVergelijkingen(){
        return vergelijkingen;
    }
}
